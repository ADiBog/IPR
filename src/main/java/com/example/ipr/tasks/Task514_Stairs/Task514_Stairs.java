package com.example.ipr.tasks.Task514_Stairs;

import java.util.Scanner;

/**
 * Задача 514: Лестница
 * 
 * Источник: https://acmp.ru/index.asp?main=task&id_task=514
 * 
 * Мальчик Петя строит из кубиков лестницу. Лестница представляет собой несколько строящихся 
 * рядом башенок из кубиков, каждая из которых ровно на один кубик выше предыдущей. 
 * Требуется по имеющемуся у мальчика Пети числу кубиков определить, какой в кубиках 
 * будет высота последней ступеньки.
 * 
 * Входные данные:
 * В единственной строке входного файла INPUT.TXT записано одно целое число K – 
 * количество кубиков у Пети (1 ≤ K ≤ 10^6).
 * 
 * Выходные данные:
 * В выходной файл OUTPUT.TXT выведите одно целое число – высоту последней ступеньки 
 * в построенной лестнице.
 * 
 * Примеры:
 * Вход: 1
 * Выход: 1
 * 
 * Вход: 2
 * Выход: 2
 * 
 * Вход: 3
 * Выход: 2
 */
public class Task514_Stairs {
    
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        
        System.out.println("=== Задача 514: Лестница ===");
        System.out.println("Источник: https://acmp.ru/index.asp?main=task&id_task=514");
        System.out.println("Построение лестницы из кубиков");
        System.out.println();
        
        // Вводим количество кубиков
        System.out.print("Введите количество кубиков у Пети (K): ");
        int k = scanner.nextInt();
        
        // Вычисляем высоту последней ступеньки
        int lastStepHeight = calculateLastStepHeight(k);
        
        // Выводим результат
        System.out.println();
        System.out.println("=== Результат ===");
        System.out.println("Высота последней ступеньки: " + lastStepHeight + " кубиков");
        
        scanner.close();
    }
    
    /**
     * Вычисляет высоту последней ступеньки в лестнице
     * 
     * Лестница строится так:
     * - 1-я ступенька: 1 кубик
     * - 2-я ступенька: 2 кубика  
     * - 3-я ступенька: 3 кубика
     * - и так далее...
     * 
     * @param block количество кубиков у Пети
     * @return высота последней ступеньки
     */
    private static int calculateLastStepHeight(int block) {
        // Проверяем ограничения задачи
        if (block < 1 || block > 1000000) {
            System.out.println("Ошибка: количество кубиков должно быть от 1 до 1,000,000");
            return -1;
        }
        
        // TODO: Реализовать алгоритм построения лестницы
        // Нужно:
        // 1. Строить ступеньки по порядку: 1, 2, 3, 4, ...
        // 2. Отнимать от общего количества кубиков высоту каждой ступеньки
        // 3. Когда кубиков не хватает для следующей ступеньки - остановиться
        // 4. Вернуть высоту последней построенной ступеньки

		int i = 1;

		while (block - i > 0) {

			block = block - i;
			i++;

		}

		return i - 1;

    }
}
